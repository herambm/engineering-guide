        Job Prospects:

Civil engineers plan and design bridges and tunnels, as well as highways, airfields, harbours, water and sewage systems, and buildings. They also supervise the construction of such projects to ensure that they are built according to carefully drafted plans. Thus they can expect to get jobs in companies that specialize in these functions, like L&T, etc.<br><br>
