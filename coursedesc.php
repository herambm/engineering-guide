Jobs offered are Core electrical, although very different from what your local electrician does. Jobs involve the production, repair, maintenance and development of electrical appliances or even planning and executing power houses. Companies like Philips, Tata Power often come in and recruit electrical engineers.<br><br>Best when done from:

