Chemical based companies like aromatics, petrochemical, washing powder etc. However, most chemical engineers go abroad to do their Masters, after which they are often hired by pharmaceutical companies, or packaged-foods companies. Students doing their Masters in fields like perfumes can hope to get placed in some of the household perfume giants.<br><br>Best when done from:

