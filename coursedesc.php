        <div class="panel-body">If you like wires and playing with circuits, go for the electronics branch. Electronics engineering involves the design and testing of electronic circuits that use the properties of components such as resistors, capacitors, inductors, diodes and transistors to achieve a particular functionality. You will be taught the basics of microprocessors, microcontrollers, their circuits, and working of the insides of a very basic level computer. You will also be given an introduction to robotics.<br><br>Job Prospects:

Core companies like Philips offer technical job profiles to candidates. Job structure involves synthesizing Integrated Circuits (ICs) maintenance, repair, and development of electronic devices or appliances like televisions, refrigerators, air conditioners, etc. No, it is NOT what electricians do!<br><br>Best when done from:

